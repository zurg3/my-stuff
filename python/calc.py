a = float(input('Enter the first number: '))
b = float(input('Enter the second number: '))

print("%g" % a + '+' + "%g" % b + '=' + "%g" % (a + b))
print("%g" % a + '-' + "%g" % b + '=' + "%g" % (a - b))
print("%g" % a + '*' + "%g" % b + '=' + "%g" % (a * b))
print("%g" % a + '/' + "%g" % b + '=' + "%g" % (a / b))
